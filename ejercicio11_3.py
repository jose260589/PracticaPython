"""Crea un programa que pida tres números por teclado.
El programa imprime en consola la media aritmética de
los números introducidos."""

print("Programa para sacar la media aritmetica de 3 numeros")
num1=int(input("Primer numero: "))
num2=int(input("segundo numero: "))
num3=int(input("tercer numero: "))

resultado=(num1+num2+num3)/3

print("La media aritmetica es: ",resultado)
