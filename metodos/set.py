#tipo de dato set, es un dato que no tiene un indice como son las listas y tuplas.

Colors = {'Red', 'Green', 'Blue'}

print(Colors)
print('Red' in Colors) #Para validar si un valor esta en una lista, tuple, o dato set.
Colors.add('Violet') #agrega un nuevo valor, pero a diferencia de la lista y tuples, este lo agrega al inicio
print(Colors)

Colors.remove('Red')
print(Colors)

